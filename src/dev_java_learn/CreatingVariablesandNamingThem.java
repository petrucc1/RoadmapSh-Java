package dev_java_learn;

public class CreatingVariablesandNamingThem {
    // É possível criar variáveis da seguinte forma:
    int numerosMarcha = 4; // Recomendado
    int $numerosMarcha = 5;
    int _numerosMarcha = 6;

    // Sempre manter o nome das variáveis o mais claro possível
    String velocidadeMaxDaBicicleta = "45km"; // Recomendado
    String vmb = "45km"; // Dificulta o entendimento do código

    // Se a variável armazena um valor constante, a convenção muda ligeiramente, capitalizando cada letra e separando as palavras subsequentes com o caractere sublinhado. Por convenção, o caractere sublinhado nunca é usado em nenhum outro lugar.
    int NUMERO_ENGRENAGENS = 6;

    // Referência: https://dev.java/learn/language-basics/variables/
}
